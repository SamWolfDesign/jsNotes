//* ASSIGNMENT OPERATOR
let r = 5;

//* ADDITION ASSIGNMENT
r += 1; //* same as r = r + 1

//* SUBTRACTION ASSIGNMENT
r -= 1 //* same as r = r - 1

//* Multiplication ASSIGNMENT
r *= 1 //* r = r *(times) 1

//* DIVISION ASSIGNMENT
r /= 1 //* same as r = r / 1

//* REMAINDER (modulus) ASSIGNMENT
r %= 1  //* same as r = r %(modulus) 1
//* Gives you the remainder back
//* Tells you what's left after you divide

//* EXPONENTIAL ASSIGNMENT OPERATOR
r **= 1 //* same as r = r ** 1

